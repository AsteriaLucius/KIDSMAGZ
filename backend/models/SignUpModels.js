const mongoose = require("mongoose");

const SignUpTemplate = new mongoose.Schema({
	uid: {
		type: String,
		required: true,
		unique: true,
	},
	username: {
		type: String,
		required: true,
		unique: true,
	},
	email: {
		type: String,
		required: true,
		unique: true,
	},
	password: {
		type: String,
		required: true,
	},
});

module.exports = mongoose.model("login", SignUpTemplate);
